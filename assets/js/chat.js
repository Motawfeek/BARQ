function detectIntent(input) {
    for (let [intent, phrases] of Object.entries(intentPhrases)) {
        if (phrases.some(phrase => phrase.test(input))) {
            return intent;
        }
    }
    return "default";
}

function getBotResponse(input) {
    let intent = detectIntent(input);
    return botResponses[intent] || botResponses.default;
}

